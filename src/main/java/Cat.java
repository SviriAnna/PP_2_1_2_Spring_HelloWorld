public class Cat {
    private String moniker;
    private String breed;
    private int age;

    public String getMoniker() {
        return moniker;
    }

    public void setMoniker(String message) {
        this.moniker = moniker;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String message) {
        this.breed = breed;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

}
